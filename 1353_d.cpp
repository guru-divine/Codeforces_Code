using namespace std;

void solve() {
    int n; cin >> n;
    set<pair<int, pii>> st;

    st.insert({-n, {0, n-1}});
    vi arr(n);
    int cnt=1;
    while(!st.empty()) {
        auto it = st.begin();
        int start = it->S.F;
        int end = it->S.S;
        int mid = (start+end)/2;
        arr[mid] = cnt++;
        st.erase(it);
        if(mid>start) st.insert({-(mid-start), {start, mid-1}});
        if(end>mid) st.insert({-(end-mid), {mid+1, end}});
    }

    printS(arr);
}

