using namespace std;

int lcm(int &a, int &b) {
    int ans = (a*b)/__gcd(a, b);
    return ans;
}

void solve() {
    int n; cin >> n;
    int ans = n;

    int num = 1;
    int idx = 1;
    while(idx<=n) {
        num = lcm(num, idx);
        if(n/num==0) break;
        ans = (ans+n/num)%mod;
        idx++;
    }

    cout << ans << nl;
}

